Digits matrix[SET_SIZE] = { 0 };

int Char2Int(char item)
{
    switch (item)
    {
        case '0': return 0;
        case '1': return 1;
        case '2': return 2;
        case '3': return 3;
        case '4': return 4;
        case '5': return 5;
        case '6': return 6;
        case '7': return 7;
        case '8': return 8;
        case '9': return 9;
        default: return 0;
    }
}

Digits GetDigits(string number)
{
    Digits digits{ 0 };
    int index = 0;
    for (char const item : number)
    {
        digits.array[index] = Char2Int(item);
        index++;
    }

    return digits;
}

int SumColumn(int column)
{
    int total = 0;
    for (int row = 0; row < SET_SIZE; row++)
    {
        total += matrix[row].array[column];
    }
    return total;
}

string SumRows()
{
    string result = "";
    int carryOver = 0;

    for (int column = NUMBER_SIZE - 1; column >= 0; column--)
    {
        int columnTotal = SumColumn(column) + carryOver;
        carryOver = columnTotal / 10;
        int digit = columnTotal % 10;
        result = to_string(digit) + result;
    }
    if (carryOver > 0) result = to_string(carryOver) + result;

    return result;
}

